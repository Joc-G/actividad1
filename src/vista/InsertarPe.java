    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String titulo = jTextField2.getText();
        String descripcion = jTextArea1.getText();

        LastUpdate.setDateFormatString("yyyy-MM-dd HH:mm:ss");
        DateFormat df = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        Date date = LastUpdate.getDate();
        String last_update = null;
        
        if (date != null) {

            last_update = df.format(date);
            System.out.println(last_update);
        }

        String lenguajePeli = Lenguaje.getSelectedItem().toString();
        String lenguajePeliOriginal = LenguajeOriginal.getSelectedItem().toString();

        if (Lenguaje.equals("---------------")) {
            JOptionPane.showMessageDialog(null, "Ingrese un lenguaje vàlido.", "Error", JOptionPane.ERROR_MESSAGE);
            continuar = false;
        }

        if (LenguajeOriginal.equals("---------------")) {
            lenguajePeliOriginal = null;
        }

        int rental_duration = -1;
        double rental_rate = -1;
        int duracion = -1;
        double costo = -1;
        int anioEstreno = -1;

        try {
            rental_duration = Integer.parseInt(jTextField4.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ingrese un valor numerico en la casilla rental duration.", "Error", JOptionPane.ERROR_MESSAGE);
            continuar = false;
        }

        try {
            rental_rate = Double.parseDouble(jTextField5.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ingrese un valor numerico en la casilla rental rate.", "Error", JOptionPane.ERROR_MESSAGE);
            continuar = false;
        }

        if (!jTextField8.getText().equals("")) {
            try {
                duracion = Integer.parseInt(jTextField8.getText());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Ingrese un valor numerico en la casilla duracion.", "Error", JOptionPane.ERROR_MESSAGE);
                continuar = false;
            }
        }

        try {
            costo = Double.parseDouble(jTextField6.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ingrese un valor numerico en la casilla Replacement cost .", "Error", JOptionPane.ERROR_MESSAGE);
            continuar = false;
        }

        if (!jTextField12.getText().equals("")) {
            try {
                anioEstreno = Integer.parseInt(jTextField12.getText());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Ingrese un valor numerico en la casilla Release year.", "Error", JOptionPane.ERROR_MESSAGE);
                continuar = false;
            }
        }

        String valoracion = Rating.getSelectedItem().toString();
        String special_features = Caracteristica.getSelectedItem().toString();

        if (titulo.equals("")) {
            JOptionPane.showMessageDialog(null, "Escriba un titulo para la película.", "Advertencia", JOptionPane.ERROR_MESSAGE);
            continuar = false;
        }

        if (lenguajePeli.equals("")) {
            JOptionPane.showMessageDialog(null, "Seleccione un lenguaje para la película.", "Advertencia", JOptionPane.ERROR_MESSAGE);
            continuar = false;
        }

        //Variable rental_duration
        if (jTextField4.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Escriba una duracion de renta para la película.", "Advertencia", JOptionPane.ERROR_MESSAGE);
            continuar = false;
        }

        //Variable rental_rate
        if (jTextField5.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Escriba una calificacion de renta para la película.", "Advertencia", JOptionPane.ERROR_MESSAGE);
            continuar = false;
        }

        //variable costo
        if (jTextField6.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Escriba un costo para la película.", "Advertencia", JOptionPane.ERROR_MESSAGE);
            continuar = false;
        }

        if (last_update.equals(null)) {
            JOptionPane.showMessageDialog(null, "Seleccione la ultima fecha de actualizacion para la película.", "Advertencia", JOptionPane.ERROR_MESSAGE);
            continuar = false;
        }

        /**
         * ****************************************
         */
        int pkLen = PKLenguaje(lenguajePeli);
        int pkLenOrg = PKLenguaje(lenguajePeliOriginal);

        if (continuar) {
            Peliculas peli = new Peliculas(titulo, descripcion, anioEstreno, pkLen, pkLenOrg, rental_duration, rental_rate, duracion, costo, valoracion, special_features, last_update);

            ControlPelicula CP = new ControlPelicula();

            boolean t = CP.InsertarPelicula(peli);
            if (t) {

                JOptionPane.showMessageDialog(null, "Datos de la imagen guardados.");
                jTextField2.setText("Ejemplo titulo");
                jTextField4.setText("5");
                jTextField5.setText("2.99");
                jTextField8.setText("120");
                jTextField6.setText("10.99");
                jTextField12.setText("1998");
                Lenguaje.setSelectedIndex(0);
                LenguajeOriginal.setSelectedIndex(0);
                Rating.setSelectedIndex(0);
                Caracteristica.setSelectedIndex(0);

            } else {

                JOptionPane.showMessageDialog(null, "Datos de la imagen no guardados.");

            }
        }


    }//GEN-LAST:event_jButton1ActionPerformed

