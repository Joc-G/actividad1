    public int PKLenguaje(String Lenguaje) {

        int pklenguaje = -1;

        for (int i = 0; i < ListaLenguajes.size(); i++) {

            if (ListaLenguajes.get(i).getName().equals(Lenguaje)) {

                pklenguaje = ListaLenguajes.get(i).getIdLenguaje();

            }

        }

        return pklenguaje;

    }

    private void traerLenguajes() {
        //Se trae todas las categorías pertenecientes a la BD
        ControlLenguaje CC2 = new ControlLenguaje();

        ListaLenguajes = CC2.LeerLenguaje();

        for (int i = 0; i < ListaLenguajes.size(); i++) {

            Lenguaje.addItem(ListaLenguajes.get(i).getName());
            LenguajeOriginal.addItem(ListaLenguajes.get(i).getName());

        }
    }

