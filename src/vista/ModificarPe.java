    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int idFilm = -1;

        try {
            idFilm = Integer.parseInt(jTextField1.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ingrese un valor numerico en la casilla ID pelicula.", "Error", JOptionPane.ERROR_MESSAGE);
        }

        if (idFilm != -1) {

            ControlPelicula CP = new ControlPelicula();

            peliEncontrada = CP.BuscarPelicula(idFilm);

            if (peliEncontrada != null) {
                encontrado = true;

                jTextField2.setText(peliEncontrada.getTitle());

                if (peliEncontrada.getDescription() != "null") {
                    jTextArea1.setText(peliEncontrada.getDescription());
                } else {
                    jTextArea1.setText("");
                }

                if (peliEncontrada.getRelease_year() != 0) {
                    jTextField9.setText(String.valueOf(peliEncontrada.getRelease_year()));
                } else {
                    jTextField9.setText("");
                }

                jTextField5.setText(String.valueOf(peliEncontrada.getRental_duration()));
                jTextField10.setText(String.valueOf(peliEncontrada.getRental_rate()));
                jTextField12.setText((peliEncontrada.getLast_update()));

                if (peliEncontrada.getLength() != 0) {
                    jTextField11.setText(String.valueOf(peliEncontrada.getLength()));
                } else {
                    jTextField11.setText("");
                }

                jTextField6.setText(String.valueOf(peliEncontrada.getReplacement_cost()));

                LenguajeFilm(peliEncontrada.getLanguage_id());
                LenguajeOriginalFilm(peliEncontrada.getOriginal_language_id());

                if (peliEncontrada.getRating() != "null") {
                    Rating.setSelectedItem(peliEncontrada.getRating());
                } else {
                    Rating.setSelectedIndex(0);
                }

                if (peliEncontrada.getSpecial_features() != "null") {
                    Caracteristica.setSelectedItem(peliEncontrada.getSpecial_features());
                } else {
                    Caracteristica.setSelectedIndex(0);
                }

            } else {
                JOptionPane.showMessageDialog(null, "No se encontó la pelicula en la base de datos.");
            }

        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int idFilm = Integer.parseInt(jTextField1.getText());
        String title = jTextField2.getText();
        String description = jTextArea1.getText();
        int language_id = PKLenguaje(Lenguaje.getSelectedItem().toString());
        int original_language_id = PKLenguaje(LenguajeOriginal.getSelectedItem().toString());
        String rating = Rating.getSelectedItem().toString();
        String special_features = Caracteristica.getSelectedItem().toString();

        LastUpdate.setDateFormatString("yyyy-MM-dd HH:mm:ss");
        DateFormat df = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        Date date = LastUpdate.getDate();
        String last_update = peliEncontrada.getLast_update();
        
        if (date != null) {
            last_update = df.format(date);
            System.out.println(last_update);
        }

        if (Lenguaje.equals("---------------")) {
            JOptionPane.showMessageDialog(null, "Ingrese un lenguaje vàlido.", "Error", JOptionPane.ERROR_MESSAGE);
            continuar = false;
        }

        if (LenguajeOriginal.equals("---------------")) {
            language_id = -1;
        }

        int rental_duration = -1;
        double rental_rate = -1;
        int length = -1;
        double replacement_cost = -1;
        int release_year = -1;

        try {
            rental_duration = Integer.parseInt(jTextField5.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ingrese un valor numerico en la casilla rental duration.", "Error", JOptionPane.ERROR_MESSAGE);
            continuar = false;
        }

        try {
            rental_rate = Double.parseDouble(jTextField10.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ingrese un valor numerico en la casilla rental rate.", "Error", JOptionPane.ERROR_MESSAGE);
            continuar = false;
        }

        if (!jTextField11.getText().equals("")) {
            try {
                length = Integer.parseInt(jTextField11.getText());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Ingrese un valor numerico en la casilla duracion.", "Error", JOptionPane.ERROR_MESSAGE);
                continuar = false;
            }
        }

        try {
            replacement_cost = Double.parseDouble(jTextField6.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ingrese un valor numerico en la casilla Replacement cost .", "Error", JOptionPane.ERROR_MESSAGE);
            continuar = false;
        }

        if (!jTextField9.getText().equals("")) {
            try {
                release_year = Integer.parseInt(jTextField9.getText());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Ingrese un valor numerico en la casilla Release year.", "Error", JOptionPane.ERROR_MESSAGE);
                continuar = false;
            }
        }

        if (title.equals("")) {
            JOptionPane.showMessageDialog(null, "Seleccione un titulo para la película.", "Advertencia", JOptionPane.ERROR_MESSAGE);
            continuar = false;
        }

        //Variable rental_duration
        if (jTextField5.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Escriba una duracion de renta para la película.", "Advertencia", JOptionPane.ERROR_MESSAGE);
            continuar = false;
        }

        //Variable rental_rate
        if (jTextField10.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Escriba una calificacion de renta para la película.", "Advertencia", JOptionPane.ERROR_MESSAGE);
            continuar = false;
        }

        //variable Replacement cost 
        if (jTextField6.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Escriba un costo para la película.", "Advertencia", JOptionPane.ERROR_MESSAGE);
            continuar = false;
        }

        if (continuar) {
            Peliculas peli = new Peliculas(title, description, release_year, language_id, original_language_id, rental_duration, rental_rate, length, replacement_cost, rating, special_features, last_update);
            ControlPelicula CP = new ControlPelicula();
            boolean t = CP.modificarPelicula(peli, idFilm);

            if (t) {

                JOptionPane.showMessageDialog(null, "Datos actualizados.");

            } else {

                JOptionPane.showMessageDialog(null, "Datos no actualizados.");

            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

