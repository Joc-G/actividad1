    public int PKLenguaje(String leng) {

        int pkLeng = -1;

        for (int i = 0; i < ListaLenguajes.size(); i++) {

            if (ListaLenguajes.get(i).getName() == leng) {

                pkLeng = ListaLenguajes.get(i).getIdLenguaje();

            }

        }
        return pkLeng;
    }

    public void LenguajeFilm(int LenguajeID) {

        for (int i = 0; i < ListaLenguajes.size(); i++) {

            if (ListaLenguajes.get(i).getIdLenguaje() == LenguajeID) {

                Lenguaje.setSelectedItem(ListaLenguajes.get(i).getName());
            }

        }

    }

    public void LenguajeOriginalFilm(int LenguajeID) {

        for (int i = 0; i < ListaLenguajes.size(); i++) {

            if (ListaLenguajes.get(i).getIdLenguaje() == LenguajeID) {

                LenguajeOriginal.setSelectedItem(ListaLenguajes.get(i).getName());
            }

        }

    }

    private void traerLenguajes() {
        //Se trae todas las categorías pertenecientes a la BD
        ControlLenguaje CC2 = new ControlLenguaje();

        ListaLenguajes = CC2.LeerLenguaje();

        for (int i = 0; i < ListaLenguajes.size(); i++) {

            Lenguaje.addItem(ListaLenguajes.get(i).getName());
            LenguajeOriginal.addItem(ListaLenguajes.get(i).getName());

        }
    }

